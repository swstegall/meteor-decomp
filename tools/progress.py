"""
Compute project progress from `config/<binary>.yaml` work pool files.

Reports:
  - per-binary  : matched / functional / wip / unmatched / middleware
  - per-module  : same breakdown, sorted descending by total size
  - overall     : matched-byte percentage, contributor-claimed count

Until Phase 1 generates the YAML files, this script reports zero.
"""

from __future__ import annotations

import json
import re
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
CONFIG_DIR = REPO_ROOT / "config"
SRC_DIR = REPO_ROOT / "src"

STATUSES = ("matched", "functional", "passthrough", "wip", "unmatched")
MIDDLEWARE = ("middleware-crt", "middleware-miles", "middleware-dx9", "middleware-stl")

RE_FUN_NAME = re.compile(r"^FUN_([0-9a-fA-F]+)\.cpp$")


def try_yaml():
    try:
        import yaml  # type: ignore
        return yaml
    except ImportError:
        return None


def _rosetta_overlay(binary_stem: str, image_base: int = 0x400000) -> tuple[int, int]:
    """Count `_rosetta/*.cpp` files for a binary as additional matches.

    The YAML is regenerated by `build_split_yaml.py` and resets all
    statuses to `unmatched`/`middleware-*`, so per-function status
    edits in the YAML don't survive a re-split. The durable artifact
    is the .cpp file under `src/<binary>/_rosetta/` — its presence
    plus `make rosetta` GREEN is the real "matched" signal.

    Returns (count, total_size_bytes) by parsing FUN_<va>.cpp filenames
    and looking up sizes in `config/<binary>.symbols.json`.
    """
    rosetta_dir = SRC_DIR / binary_stem / "_rosetta"
    if not rosetta_dir.is_dir():
        return (0, 0)
    sym_path = CONFIG_DIR / f"{binary_stem}.symbols.json"
    if not sym_path.exists():
        return (0, 0)
    syms = json.loads(sym_path.read_text())
    rva_to_size = {s["rva"]: int(s.get("size", 0)) for s in syms}

    count = 0
    total_size = 0
    for cpp in rosetta_dir.glob("FUN_*.cpp"):
        m = RE_FUN_NAME.match(cpp.name)
        if not m:
            continue
        va = int(m.group(1), 16)
        rva = va - image_base
        size = rva_to_size.get(rva, 0)
        if size > 0:
            count += 1
            total_size += size
    return (count, total_size)


def main() -> int:
    yaml = try_yaml()
    if yaml is None:
        print("note: PyYAML not installed (`pip install pyyaml`)", file=sys.stderr)
        # Soft-fail; print the placeholder so make doesn't abort.

    yamls = sorted(CONFIG_DIR.glob("*.yaml")) if CONFIG_DIR.exists() else []
    if not yamls:
        print("(no work-pool yaml found yet — Phase 1 deliverable)")
        return 0

    if yaml is None:
        print("(install PyYAML to summarise)", file=sys.stderr)
        return 0

    grand = {s: {"count": 0, "bytes": 0} for s in (*STATUSES, *MIDDLEWARE)}
    for path in yamls:
        rows = yaml.safe_load(path.read_text()) or []
        if not isinstance(rows, list):
            continue
        per = {s: {"count": 0, "bytes": 0} for s in (*STATUSES, *MIDDLEWARE)}
        for r in rows:
            status = r.get("status", "unmatched")
            size = int(r.get("size", 0))
            tier = r.get("type", "matching")
            key = tier if tier in MIDDLEWARE else status
            if key in per:
                per[key]["count"] += 1
                per[key]["bytes"] += size
                grand[key]["count"] += 1
                grand[key]["bytes"] += size

        print(f"=== {path.name} ===")
        for k, v in per.items():
            if v["count"]:
                print(f"  {k:18s}  count={v['count']:>6d}  bytes={v['bytes']:>10,}")

        # _rosetta/ overlay — durable count of matched .cpp files
        # (survives YAML regeneration). Subtract the YAML's matched
        # count to get the "additional" landed via _rosetta/*.cpp,
        # since some YAML matches (middleware-*) are also auto-tagged
        # without a corresponding .cpp.
        binary_stem = path.stem
        rosetta_count, rosetta_bytes = _rosetta_overlay(binary_stem)
        if rosetta_count:
            print(f"  {'_rosetta/*.cpp':18s}  count={rosetta_count:>6d}  bytes={rosetta_bytes:>10,}  "
                  f"(durable, may overlap with YAML 'matched')")

    total_bytes = sum(v["bytes"] for v in grand.values())
    matched_bytes = grand["matched"]["bytes"] + grand["functional"]["bytes"]
    passthrough_bytes = grand["passthrough"]["bytes"]
    covered_bytes = matched_bytes + passthrough_bytes
    if total_bytes:
        pct_m = 100.0 * matched_bytes / total_bytes
        pct_p = 100.0 * passthrough_bytes / total_bytes
        pct_total = 100.0 * covered_bytes / total_bytes
        print(f"\noverall (YAML status):  matched/functional = {matched_bytes:,} / {total_bytes:,} bytes  ({pct_m:.2f}%)")
        if passthrough_bytes:
            print(f"                        passthrough        = {passthrough_bytes:,} bytes  ({pct_p:.2f}%)")
            print(f"                        relinkable cover   = {covered_bytes:,} / {total_bytes:,} bytes  ({pct_total:.2f}%)")

    # Cross-binary _rosetta overlay totals — the durable artifact.
    overlay_total_count = 0
    overlay_total_bytes = 0
    for path in yamls:
        c, b = _rosetta_overlay(path.stem)
        overlay_total_count += c
        overlay_total_bytes += b
    if overlay_total_count:
        pct = 100.0 * overlay_total_bytes / total_bytes if total_bytes else 0.0
        print(f"overall (_rosetta/*.cpp): {overlay_total_count:,} files = {overlay_total_bytes:,} bytes  ({pct:.2f}%)")

    return 0


if __name__ == "__main__":
    sys.exit(main())
