def process_binary(stem: str, dry_run: bool, image_base: int = 0x400000) -> dict:
    rosetta_dir = SRC_DIR / stem / "_rosetta"
    rosetta_dir.mkdir(parents=True, exist_ok=True)
    asm_dir = ASM_ROOT / stem

    # Read both clusters JSONs (exact-byte + reloc-aware), unioning members.
    clusters_paths = [
        EASY_WINS / f"{stem}.clusters.json",
        EASY_WINS / f"{stem}.clusters_reloc.json",
    ]
    seen_rva: set[int] = set()
    n_recognised = 0
    n_skipped_unrecognised = 0
    n_skipped_existing = 0
    by_idiom: dict[str, int] = {}

    for p in clusters_paths:
        if not p.exists():
            continue
        clusters = json.loads(p.read_text())
        for h, members in clusters.items():
            # Pick the smallest-RVA member as primary, deterministic per cluster.
            rep = min(members, key=lambda m: m["rva"])
            if rep["rva"] in seen_rva:
                continue
            seen_rva.add(rep["rva"])
            asm_path = asm_dir / rep["asm"]
            body = read_function_bytes(asm_path)
            if body is None:
                continue
            va = rep["rva"] + image_base
            target_path = rosetta_dir / f"FUN_{va:08x}.cpp"
            if target_path.exists():
                n_skipped_existing += 1
                continue
            result = derive_one(body, va, len(members))
            if result is None:
                n_skipped_unrecognised += 1
                continue
            src, idiom = result
            if not dry_run:
                target_path.write_text(src)
            n_recognised += 1
            by_idiom[idiom] = by_idiom.get(idiom, 0) + 1
    return {
        "binary": stem,
        "recognised": n_recognised,
        "skipped_existing": n_skipped_existing,
        "skipped_unrecognised": n_skipped_unrecognised,
        "by_idiom": by_idiom,
    }


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("binary", default="ffxivgame", nargs="?")
    ap.add_argument("--all", action="store_true", help="run on all 5 binaries")
    ap.add_argument("--dry-run", action="store_true")
    args = ap.parse_args()

    if args.all:
        targets = list(KNOWN_BINARIES)
    else:
        targets = [args.binary.replace(".exe", "")]

    grand_recognised = 0
    grand_idioms: dict[str, int] = {}
    for t in targets:
        report = process_binary(t, dry_run=args.dry_run)
        print(f"=== {t} ===")
        print(f"  recognised:           {report['recognised']:>5}{' (dry-run)' if args.dry_run else ''}")
        print(f"  skipped (existing):   {report['skipped_existing']:>5}")
        print(f"  skipped (unmatched):  {report['skipped_unrecognised']:>5}")
        if report["by_idiom"]:
            print(f"  by idiom:")
            for idiom, n in sorted(report["by_idiom"].items(), key=lambda kv: -kv[1]):
                print(f"    {idiom:<35}  {n}")
                grand_idioms[idiom] = grand_idioms.get(idiom, 0) + n
        grand_recognised += report["recognised"]

    if len(targets) > 1:
        print(f"\nGrand total recognised: {grand_recognised}{' (dry-run)' if args.dry_run else ''}")
        print(f"Top idioms across all binaries:")
        for idiom, n in sorted(grand_idioms.items(), key=lambda kv: -kv[1])[:15]:
            print(f"  {idiom:<35}  {n}")

    return 0


if __name__ == "__main__":
    sys.exit(main())
