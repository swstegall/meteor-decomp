def _header(va: int, idiom: str, asm_summary: str, n_members: int) -> str:
    return (
        f"{LICENSE}\n"
        f"// SPDX-License-Identifier: AGPL-3.0-or-later\n"
        f"//\n"
        f"// FUNCTION: ffxivgame {hex(va)} — {idiom}\n"
        f"// Auto-generated by tools/derive_templates.py.\n"
        f"// Cluster representative — covers {n_members} byte-identical (or\n"
        f"// reloc-equivalent) members.\n"
        f"//\n"
        f"// Asm: {asm_summary}\n"
    )


def try_empty_void(body: bytes, va: int, n: int) -> tuple[str, str] | None:
    if body == b"\xc3":
        src = (
            _header(va, "empty zero-arg method (RET)", "c3", n)
            + "\nclass C { public: void empty(); };\n"
              "void C::empty() {}\n"
        )
        return (src, "empty 0-arg")
    return None


def try_empty_n_arg(body: bytes, va: int, n: int) -> tuple[str, str] | None:
    if len(body) == 3 and body[0] == 0xc2 and body[2] == 0x00:
        nbytes = body[1]
        if nbytes == 0 or nbytes % 4 != 0:
            return None
        nargs = nbytes // 4
        args = ", ".join(["int"] * nargs)
        src = (
            _header(va, f"empty `__thiscall` {nargs}-arg method (RET 0x{nbytes:x})",
                    f"c2 {nbytes:02x} 00", n)
            + f"\nclass C {{ public: void empty{nargs}({args}); }};\n"
              f"void C::empty{nargs}({args}) {{}}\n"
        )
        return (src, f"empty {nargs}-arg")
    return None


