def _padding_for_offset(offset: int, base_t: str = "int") -> str:
    """Generate `T padding[N];` for byte-offset `offset` of base T (4 B for int)."""
    if offset == 0:
        return ""
    base_size = 4 if base_t == "int" else (1 if base_t == "char" else 4)
    if offset % base_size != 0:
        # Use char[] for non-aligned offsets
        return f"char padding[{offset}];"
    n = offset // base_size
    return f"{base_t} padding[{n}];"


def try_int_getter(body: bytes, va: int, n: int) -> tuple[str, str] | None:
    # 4-byte form: 8b 41 NN c3 — MOV EAX, [ECX+NN]; RET
    if len(body) == 4 and body[0] == 0x8b and body[1] == 0x41 and body[3] == 0xc3:
        offset = body[2]
        if offset == 0:
            # 8b 41 00 wouldn't happen — that'd be 8b 01. Skip.
            return None
        pad = _padding_for_offset(offset, "int")
        if pad and offset % 4 != 0:
            # Non-aligned — skip; rare and source generation would be tricky.
            return None
        src = (
            _header(va, f"int field getter at offset 0x{offset:x}",
                    f"8b 41 {offset:02x} c3", n)
            + f"\nclass C {{\n"
            + (f"    {pad}\n" if pad else "")
            + f"    int field;\npublic:\n    int get_field();\n}};\n\n"
              f"int C::get_field() {{ return field; }}\n"
        )
        return (src, f"int getter @ 0x{offset:x}")
    return None


def try_short_zero_ext_getter(body: bytes, va: int, n: int) -> tuple[str, str] | None:
    # 0f b7 41 NN c3 — MOVZX EAX, word ptr [ECX+NN]; RET (zero-ext to int)
    if len(body) == 5 and body[0:2] == b"\x0f\xb7" and body[2] == 0x41 and body[4] == 0xc3:
        offset = body[3]
        if offset == 0:
            return None
        if offset % 2 != 0:
            return None
        pad = _padding_for_offset(offset, "char")  # char[] for byte offsets
        src = (
            _header(va, f"unsigned short field getter at offset 0x{offset:x} (zero-ext to int)",
                    f"0f b7 41 {offset:02x} c3", n)
            + f"\nclass C {{\n"
            + (f"    {pad}\n" if pad else "")
            + f"    unsigned short field;\npublic:\n    unsigned int get_field();\n}};\n\n"
              f"unsigned int C::get_field() {{ return field; }}\n"
        )
        return (src, f"u16 getter @ 0x{offset:x}")
    return None


