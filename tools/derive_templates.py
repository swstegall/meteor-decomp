            + "\nextern \"C\" int target();\n\n"
            + "extern \"C\" __declspec(naked) void unwind_thunk() {\n"
            + "    __asm {\n"
            + f"        mov ecx, [ebp {ebp_sign} {ebp_mag}]\n"
            + f"        add ecx, {add_imm}\n"
            + "        jmp target\n"
            + "    }\n"
            + "}\n"
