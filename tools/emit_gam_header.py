"""
Emit a C++ header that declares the GAM CompileTimeParameter registry
as a `constexpr` table — one struct per Data class plus a per-class
const array of `(id, type_kind, byte_size)` tuples. This is the
source-of-truth schema that future code (Rust ↔ C++ FFI, validation
tools, etc.) can include.

Reads:
  config/<binary>.gam_params.json  (from extract_gam_params.py)

Writes:
  include/net/gam_registry.h
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from collections import defaultdict
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
CONFIG = REPO_ROOT / "config"
INCLUDE = REPO_ROOT / "include" / "net"

# Map our friendly type strings to (C++ type, TypeKind enum, element size in bytes).
PRIMITIVE_TYPES: dict[str, tuple[str, str, int]] = {
    "signed char":      ("std::int8_t",   "I8",   1),
    "char":             ("char",          "I8",   1),
    "unsigned char":    ("std::uint8_t",  "U8",   1),
    "short":            ("std::int16_t",  "I16",  2),
    "unsigned short":   ("std::uint16_t", "U16",  2),
    "int":              ("std::int32_t",  "I32",  4),
    "unsigned int":     ("std::uint32_t", "U32",  4),
    "long":             ("std::int32_t",  "I32",  4),
    "unsigned long":    ("std::uint32_t", "U32",  4),
    "float":            ("float",         "F32",  4),
    "double":           ("double",        "F64",  8),
    "__int64":          ("std::int64_t",  "I64",  8),
    "unsigned __int64": ("std::uint64_t", "U64",  8),
    "bool":             ("bool",          "BOOL", 1),
    "wchar_t":          ("wchar_t",       "U16",  2),
    "Sqex::Misc::Utf8String": ("sqex::Utf8String", "UTF8_STRING", 0),  # variable-length
}

RE_ARRAY = re.compile(r"^(.+)\[(\d+)\]$")
RE_BLOB  = re.compile(r"^Blob<(\d+)>$")
RE_BLOB_ARRAY = re.compile(r"^Blob<(\d+)>\[(\d+)\]$")


def cpp_for_type(t: str) -> tuple[str, str, int, int]:
    """Return (cpp_type_decl, type_kind, elem_size, total_bytes)."""
    if t in PRIMITIVE_TYPES:
        cpp, kind, sz = PRIMITIVE_TYPES[t]
        return (cpp, kind, sz, sz)
    m = RE_ARRAY.match(t)
    if m:
        elem, n = m.group(1), int(m.group(2))
        if elem in PRIMITIVE_TYPES:
            cpp, kind, sz = PRIMITIVE_TYPES[elem]
            return (f"std::array<{cpp}, {n}>", f"ARRAY_{kind}", sz, sz * n)
    m = RE_BLOB.match(t)
    if m:
        n = int(m.group(1))
        return (f"std::array<std::uint8_t, {n}>", "BLOB", 1, n)
    m = RE_BLOB_ARRAY.match(t)
    if m:
        blob_size, n = int(m.group(1)), int(m.group(2))
        return (f"std::array<std::array<std::uint8_t, {blob_size}>, {n}>", "ARRAY_BLOB", blob_size, blob_size * n)
    return (f"/* unhandled: {t} */ void", "UNKNOWN", 0, 0)


def sanitize_class_name(ns: str) -> str:
    """Take 'Application::Network::GameAttributeManager::Data::Player' and
    return 'Player' as the C++ leaf class name."""
    return ns.split("::")[-1]


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("binary", help="binary stem, e.g. ffxivgame")
    args = ap.parse_args()
    stem = args.binary.replace(".exe", "")

    src = CONFIG / f"{stem}.gam_params.json"
    if not src.exists():
        print(f"error: missing {src}; run extract_gam_params.py first", file=sys.stderr)
        return 1

    rows = json.loads(src.read_text())
    rows.sort(key=lambda r: (r["ns"], r["id"]))

    by_ns: dict[str, list[dict]] = defaultdict(list)
    for r in rows:
        by_ns[r["ns"]].append(r)

    INCLUDE.mkdir(parents=True, exist_ok=True)
    out = INCLUDE / "gam_registry.h"

    with out.open("w") as f:
        f.write("""// meteor-decomp — clean-room decompilation of FINAL FANTASY XIV 1.x client binaries
// Copyright (C) 2026  Samuel Stegall
//
// This program is free software: you can redistribute it and/or modify
// it under the terms of the GNU Affero General Public License as published
// by the Free Software Foundation, either version 3 of the License, or
// (at your option) any later version.
//
// SPDX-License-Identifier: AGPL-3.0-or-later
//
// AUTO-GENERATED by tools/emit_gam_header.py — DO NOT EDIT BY HAND.
//
// The GAM (Game Attribute Manager) registry: the binary's compile-time
// schema for wire-serializable game-state Data classes used by the
// FFXIV 1.x client's *lobby* protocol (CharaMakeData / ClientSelectData
// / Player / PlayerPlayer / ZoneInitData).
//
// IMPORTANT: this is a *different* wire system from the in-game
// SetActorPropertyPacket (which uses Murmur2(name) hashes as 32-bit
// wire ids). GAM uses small ordinal ids (100-345 + 579-595) within
// each Data class's namespace. See `docs/wire-protocol.md` for the
// architectural distinction.
//
// Source: ffxivgame.exe RTTI .rdata strings parsed by
// tools/extract_gam_params.py. 192 unique (id, namespace) entries
// recovered as of the 2026-04-30 Phase-1 dump. Counts here may drift
// slightly across rebuilds — re-run `make extract-gam` to refresh.

#pragma once

#include <array>
#include <cstdint>
#include <string_view>

""")

        f.write("namespace meteor_decomp::gam {\n\n")
        f.write("enum class TypeKind : std::uint8_t {\n")
        kinds = sorted({cpp_for_type(r["type"])[1] for r in rows})
        for k in kinds:
            f.write(f"    {k},\n")
        f.write("};\n\n")

        f.write("constexpr std::string_view type_kind_name(TypeKind k) {\n")
        f.write("    switch (k) {\n")
        for k in kinds:
            f.write(f'        case TypeKind::{k}: return "{k}";\n')
        f.write('        default: return "?";\n')
        f.write("    }\n")
        f.write("}\n\n")

        f.write("struct ParamDescriptor {\n")
        f.write("    std::uint16_t id;        // ordinal within the Data class\n")
        f.write("    TypeKind      kind;\n")
        f.write("    std::uint16_t element_size;  // bytes per element (0 for variable-length Utf8String)\n")
        f.write("    std::uint32_t total_bytes;   // wire footprint (0 for variable-length)\n")
        f.write('    std::string_view name;       // property name from the binary\n')
        f.write('    std::string_view raw_type;   // raw type token (incl. unhandled templates)\n')
        f.write("};\n\n")

        for ns in sorted(by_ns):
            cls = sanitize_class_name(ns)
            ns_rows = by_ns[ns]
            named = sum(1 for r in ns_rows if r.get("paramname"))
            f.write(f"// {ns} — {len(ns_rows)} parameters ({named} named).\n")
            f.write(f"namespace {cls.lower()}_params {{\n\n")
            f.write(f"constexpr std::array<ParamDescriptor, {len(ns_rows)}> kRegistry {{{{\n")
            for r in ns_rows:
                _, kind, elem_size, total = cpp_for_type(r["type"])
                name = r.get("paramname") or ""
                # Escape any " inside the name (shouldn't happen but be safe).
                name_lit = name.replace("\\", "\\\\").replace('"', '\\"')
                f.write(f'    {{ {r["id"]}, TypeKind::{kind}, {elem_size}, {total}, "{name_lit}", "{r["type"]}" }},\n')
            f.write("}};\n\n")
            f.write(f"}}  // namespace {cls.lower()}_params\n\n")

        f.write("}  // namespace meteor_decomp::gam\n")

    print(f"wrote: {out.relative_to(REPO_ROOT)}  ({sum(1 for _ in by_ns)} Data classes, {len(rows)} parameters)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
