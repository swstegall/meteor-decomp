"""
Emit one .cpp per non-`.text` PE section, packing the orig bytes into
a single `__declspec(allocate(...))` byte array. The linker concatenates
the array into the matching output section, giving us byte-identical
content for `.rdata`, `.data`, `.rsrc`, `.tls`, `MSSMIXER` etc. without
re-deriving any of their internal structure.

Why: the recompilable-client effort focuses source-level decomp on
`.text` (functions). Non-code sections — string tables, vtables, IAT
imports, embedded resources, the Miles Sound System mixer blob — are
opaque blobs as far as we're concerned. Emitting them verbatim from
orig is the lowest-risk path to a byte-identical re-link.

Generated structure (one file per section):

  src/<bin>/_passthrough/_section_<name>.cpp:
    #pragma section(".rdata$X00100000", read)
    extern "C" __declspec(allocate(".rdata$X00100000"))
    const unsigned char _section_rdata[N] = { 0x.., 0x.., ... };
    #pragma comment(linker, "/INCLUDE:__section_rdata")

The `$X<rva>` keyed subsection name mirrors the pattern used by the
function-passthrough emitter (`tools/emit_passthrough_cpp.py`) so the
linker's lexicographic-sort packing lands the blob at its orig RVA
when combined with `/MERGE:.rdata$X*=.rdata`.

Reads:
  build/pe-layout/<binary>.json — section table
  orig/<binary>.exe             — section bytes

Writes:
  src/<binary>/_passthrough/_section_<name>.cpp     (one per section)
  build/wire/<binary>.data_sections.md              (manifest)

Usage:
  tools/emit_data_sections.py ffxivlogin
  tools/emit_data_sections.py ffxivlogin --dry-run
  tools/emit_data_sections.py            # all five binaries

Notes:
  - We DON'T emit `.text` here — that's `emit_passthrough_cpp.py`
    + `emit_text_gaps.py`'s job.
  - `.rsrc` is emitted as a raw byte blob too. A higher-fidelity
    path would use `rc.exe` with a generated `.rc` source so the
    PE's resource directory tree is populated correctly. The
    byte-blob path is the MVP; resource-directory fidelity comes
    later.
  - PE alignment: each section in orig is padded out to file_alignment
    (often 0x1000 = 4 KB). We emit only the `raw_size` bytes; the
    linker handles padding via section alignment when laying out
    the merged output.
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
PE_LAYOUT = REPO_ROOT / "build" / "pe-layout"
ORIG = REPO_ROOT / "orig"
SRC = REPO_ROOT / "src"
WIRE = REPO_ROOT / "build" / "wire"

ALL_BINARIES = ("ffxivboot", "ffxivconfig", "ffxivgame", "ffxivlogin", "ffxivupdater")

# Sections we byte-emit. With INCLUDE_TEXT=True, also emit `.text` as
# a single byte blob — bypasses the function-passthrough subsection
# pipeline entirely. Coarser-grained but link.exe lays it down
# byte-identical to orig with no inter-section padding artifacts.
EMITTABLE_SECTIONS = (".rdata", ".data", ".rsrc", ".tls", ".reloc", "MSSMIXER")
INCLUDE_TEXT = False  # toggle via --include-text CLI flag

# Per-section pragma access flags. Keys map to MSVC `#pragma section`
# access lists. `.rsrc` doesn't have a write flag but cl.exe rejects
# an empty access list, so we use `read`.
SECTION_FLAGS = {
    ".text":    "read,execute",
    ".rdata":   "read",
    ".data":    "read,write",
    ".rsrc":    "read",
    ".tls":     "read,write",
    ".reloc":   "read,discard",
    "MSSMIXER": "read,execute",
}

LICENSE_HEADER = """\
// meteor-decomp — clean-room decompilation of FINAL FANTASY XIV 1.x client binaries
// Copyright (C) 2026  Samuel Stegall
//
// This program is free software: you can redistribute it and/or modify
// it under the terms of the GNU Affero General Public License as published
// by the Free Software Foundation, either version 3 of the License, or
// (at your option) any later version.
//
// SPDX-License-Identifier: AGPL-3.0-or-later
"""


def _safe_section_id(name: str) -> str:
    """Section names like `.rdata` and `MSSMIXER` need to map to a
    valid C identifier for the global array's symbol. Replace
    non-alphanumeric with underscore; prefix with `_` if it starts
    with a digit (none of ours do)."""
    out = "".join(c if c.isalnum() else "_" for c in name)
    return out


def _emit_section_cpp(binary: str, section: dict, body: bytes) -> str:
    sec_name = section["name"]
    rva = section["virtual_address"]
    raw_size = section["raw_size"]
    flags = SECTION_FLAGS.get(sec_name, "read")
    sub_name = f"{sec_name}$X{rva:08x}"
    sym = f"_section_{_safe_section_id(sec_name)}"
    out: list[str] = [LICENSE_HEADER, ""]
    out.append(f"// AUTO-GENERATED by tools/emit_data_sections.py — do not edit.")
    out.append(f"// {binary}.exe section blob.")
    out.append(f"//   section name : {sec_name}")
    out.append(f"//   subsection   : {sub_name}")
    out.append(f"//   rva          : 0x{rva:08x}")
    out.append(f"//   raw_size     : {raw_size:,} bytes (= virtual_size {section['virtual_size']:,} after section alignment)")
    out.append(f"//   characteristics: {section['characteristics_decoded']}")
    out.append("")
    out.append(f'#pragma section("{sub_name}", {flags})')
    # link.exe automatically merges grouped sections (`<sec>$<key>` →
    # `<sec>`) sorted lexicographically by `<key>`. We don't need an
    # explicit /MERGE — link.exe's wildcard-rejection (LNK1184) blocks
    # the obvious `/MERGE:<sec>$X*=<sec>` form anyway.
    # /INCLUDE keeps the array alive across /OPT:REF; the linker
    # symbol gets the cdecl `_` prefix on x86 so we add a second `_`.
    out.append(f'#pragma comment(linker, "/INCLUDE:_{sym}")')
    out.append(f'extern "C" __declspec(allocate("{sub_name}"))')
    out.append(f'const unsigned char {sym}[{raw_size}] = {{')
    # Pretty-print 16 bytes per line.
    for i in range(0, raw_size, 16):
        chunk = body[i:i + 16]
        bytes_str = ", ".join(f"0x{b:02x}" for b in chunk)
        out.append(f'    {bytes_str},')
    out.append("};")
    out.append("")
    return "\n".join(out)


def _emit_report(binary: str, written: list[tuple[str, int, Path]]) -> str:
    out: list[str] = []
    out.append(f"# {binary}.exe — non-code section blob manifest\n")
    out.append("Auto-generated by `tools/emit_data_sections.py`.\n")
    out.append("Each row is one section emitted to a `.cpp` whose")
    out.append("`__declspec(allocate(\"<sec>$X<rva>\"))` array round-trips the orig")
    out.append("section bytes. The linker `/MERGE:<sec>$X*=<sec>` collapses each")
    out.append("array back into the merged output section.\n")
    out.append("| section | rva | raw_size | output |")
    out.append("|---|---|---:|---|")
    for sec, raw_size, path in written:
        out.append(f"| `{sec}` | 0x{path.stat().st_size if path.exists() else 0:08x} | {raw_size:,} | `{path.relative_to(REPO_ROOT)}` |")
    return "\n".join(out) + "\n"


def process(binary: str, dry_run: bool, include_text: bool = False) -> dict:
    pe_path = PE_LAYOUT / f"{binary}.json"
    orig_path = ORIG / f"{binary}.exe"
    if not pe_path.exists() or not orig_path.exists():
        return {"binary": binary, "skipped": "missing PE layout or orig"}
    pe = json.loads(pe_path.read_text())
    orig_bytes = orig_path.read_bytes()
    out_dir = SRC / binary / "_passthrough"
    out_dir.mkdir(parents=True, exist_ok=True)

    section_set = set(EMITTABLE_SECTIONS)
    if include_text:
        section_set.add(".text")

    written: list[tuple[str, int, Path]] = []
    for sec in pe["sections"]:
        sec_name = sec["name"]
        if sec_name not in section_set:
            continue
        raw_size = sec["raw_size"]
        raw_ptr = sec["raw_pointer"]
        if raw_size == 0:
            continue
        body = orig_bytes[raw_ptr:raw_ptr + raw_size]
        cpp = _emit_section_cpp(binary, sec, body)
        out_path = out_dir / f"_section_{_safe_section_id(sec_name)}.cpp"
        if not dry_run:
            out_path.write_text(cpp)
        written.append((sec_name, raw_size, out_path))

    report_path = WIRE / f"{binary}.data_sections.md"
    if not dry_run:
        report_path.parent.mkdir(parents=True, exist_ok=True)
        report_path.write_text(_emit_report(binary, written))
    return {
        "binary": binary,
        "sections": [w[0] for w in written],
        "total_bytes": sum(w[1] for w in written),
        "report": str(report_path.relative_to(REPO_ROOT)),
    }


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("binary", nargs="?", help="binary stem (default: all five)")
    ap.add_argument("--dry-run", action="store_true")
    ap.add_argument("--include-text", action="store_true",
                    help="ALSO emit `.text` as one big byte blob — bypasses the function-passthrough subsection pipeline. Coarser-grained but link.exe lays it down byte-identical with no inter-section padding artifacts. Excludes any function-level rosetta substitution.")
    args = ap.parse_args()
    binaries = (args.binary,) if args.binary else ALL_BINARIES
    print(f"=== emit_data_sections (dry-run={args.dry_run}, include_text={args.include_text}) ===")
    for stem in binaries:
        res = process(stem, args.dry_run, include_text=args.include_text)
        if "skipped" in res:
            print(f"  {stem}: SKIPPED ({res['skipped']})")
            continue
        print(f"  {stem}: sections={res['sections']} bytes={res['total_bytes']:,}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
